	private void cargarVertedero(){

		try{
			_vVertederos= (new OperacionesContaminantes(com.geopista.app.contaminantes.Constantes.url)).getVertederos();
			if (_vVertederos==null){
				logger.warn("No existen vertederos en el sistema");
				UtilsContaminantes.mostrarMensaje(jPanelDatos, messages.getString("jMenuItemVertederoInformes.mensaje1"), messages.getString("jMenuItemVertederoInformes.mensajeTittle1"));
				jButtonGenerarInformes.setEnabled(false);
			}else{
				lista_expedientes = "";
				for (int i = 0; i < _vVertederos.size(); i++) {
					Vertedero vertedero = (Vertedero) _vVertederos.elementAt(i);
					lista_expedientes += "'"+vertedero.getId()+"'";
					if (i < _vVertederos.size()-1){
						lista_expedientes += ", ";
					}
				}   

				actualizarModelo();
			}

		}catch(Exception e){
			StringWriter sw = new StringWriter();
			PrintWriter pw = new PrintWriter(sw);
			e.printStackTrace(pw);
			logger.error("Exception: " + sw.toString());
		}
	}


	private void salir(){
		/** volvemos a registrar el driver GEOPISTADriver */
		String classForName= "com.geopista.sql.GEOPISTADriver";
		try{
			Driver dPista= (Driver)Class.forName(classForName).newInstance();
			DriverManager.registerDriver(dPista);
		}catch(Exception e){
			logger.debug(".................Class.forNmae " + classForName + " " + e.getMessage());
		}

		dispose();
	}

