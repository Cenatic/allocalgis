	private void actualizarModelo(){
		modelVertedero.setModelData(_vVertederos);
		sorter = new TableSorted(modelVertedero);
		sorter.setTableHeader(jTableListado.getTableHeader());
		jTableListado.setModel(sorter);
		jTableListado.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
	}

	
	private void generarInforme() {
		this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
		try {
			if ((_vVertederos != null) && (_vVertederos.size() > 0) )
			{
				String report = ((String)plantillaJCBox.getSelectedItem());
				if (report != null && !report.equals("")){    
					Map parametros = obtenerParametros();
					String reportFile = UtilsContaminantes.getLocalPathPlantillas() + report;
					GenerarInformeExterno giep=new GenerarInformeExterno(reportFile, parametros);
				}
			}
			else
				UtilsContaminantes.mostrarMensaje(jPanelDatos, messages.getString("jMenuItemVertederoInformes.mensaje4"), messages.getString("jMenuItemVertederoInformes.mensajeTittle1"));
		}catch(Exception e){
			this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
			UtilsContaminantes.mostrarMensaje(jPanelDatos, messages.getString("jMenuItemVertederoInformes.mensaje2"), messages.getString("jMenuItemVertederoInformes.mensajeTittle1"));
			StringWriter sw = new StringWriter();
			PrintWriter pw = new PrintWriter(sw);
			e.printStackTrace(pw);
			logger.error("Exception: " + sw.toString());
		}
		this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
	}

