package com.geopista.app.cementerios.business.vo;

public class Comun {
	
	
	private String attname;
	private String format_type;
	private String attrelid;
	private int attlen;
	
	
	public String getAttname() {
		return attname;
	}
	public void setAttname(String attname) {
		this.attname = attname;
	}
	public String getFormat_type() {
		return format_type;
	}
	public void setFormat_type(String format_type) {
		this.format_type = format_type;
	}
	public String getAttrelid() {
		return attrelid;
	}
	public void setAttrelid(String attrelid) {
		this.attrelid = attrelid;
	}
	public int getAttlen() {
		return attlen;
	}
	public void setAttlen(int attlen) {
		this.attlen = attlen;
	}
	

}
