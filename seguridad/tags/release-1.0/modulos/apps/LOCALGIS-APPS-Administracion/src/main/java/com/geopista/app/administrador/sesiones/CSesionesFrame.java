     private void actualizarModelo()
    {
        modelSesiones= new SesionesTableModel(checkPermission);
        modelSesiones.setModelData(listaSesiones);
        sorter = new TableSorted(modelSesiones);
        sorter.setTableHeader(jTableSesion.getTableHeader());
        jTableSesion.setModel(sorter);
        jTableSesion.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    public void changeScreenLang(ResourceBundle messages) {
        jButtonSalir.setText(messages.getString("CSesionesFrame.jButtonSalir"));//"Desmarcar todos");
        jButtonSalir.setToolTipText(messages.getString("CSesionesFrame.jButtonSalir"));

        TableColumn tableColumn= jTableSesion.getColumnModel().getColumn(0);
        tableColumn.setHeaderValue(messages.getString("CSesionesFrame.col0"));
        tableColumn= jTableSesion.getColumnModel().getColumn(1);
        tableColumn.setHeaderValue(messages.getString("CSesionesFrame.col1"));
        tableColumn= jTableSesion.getColumnModel().getColumn(2);
        tableColumn.setHeaderValue(messages.getString("CSesionesFrame.col2"));
        tableColumn= jTableSesion.getColumnModel().getColumn(3);
        tableColumn.setHeaderValue(messages.getString("CSesionesFrame.col3"));

    }

