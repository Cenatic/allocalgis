    public void load(Inspeccion inspeccion)
    {
         jTextFieldMax.setText(inspeccion.getCmax());
         jTextFieldMin.setText(inspeccion.getCmin());
         Calendar auxCalendar =Calendar.getInstance();
         if (inspeccion.getFfin()!=null)
               auxCalendar.setTime(inspeccion.getFfin());
         else
              auxCalendar=null;
         jButtonFechaFin.setFecha(auxCalendar);
         auxCalendar =Calendar.getInstance();
         if (inspeccion.getFinicio()!=null)
               auxCalendar.setTime(inspeccion.getFinicio());
         else
              auxCalendar=null;
         jButtonFechaInicio.setFecha(auxCalendar);
        auxCalendar =Calendar.getInstance();
        if (inspeccion.getFfindatos()!=null)
               auxCalendar.setTime(inspeccion.getFfindatos());
        else
                     auxCalendar=null;
        jButtonFechaRecFin.setFecha(auxCalendar);

        auxCalendar =Calendar.getInstance();
        if (inspeccion.getFinidatos()!=null)
               auxCalendar.setTime(inspeccion.getFinidatos());
        else
               auxCalendar=null;
        jButtonFechaRecInicio.setFecha(auxCalendar);
        jTextPaneRiesgos.setText(inspeccion.getFriesgo());
        jTextPaneMedidas.setText(inspeccion.getMedidas());
        jTextPaneZonaLatente.setText(inspeccion.getMotlantente());
        jTextPaneZonaSaturada.setText(inspeccion.getMotsaturada());
        if (inspeccion.getNfolios()!=null)
            jTextFieldNumFolios.setNumber(inspeccion.getNfolios());
        else
            jTextFieldNumFolios.setText("");
        if (inspeccion.getNrec()!=null)
            jTextFieldRecDias.setNumber(inspeccion.getNrec());
        else
            jTextFieldRecDias.setText("");
        jTextPaneObs.setText(inspeccion.getObs());
        jTextFieldFijos.setText(inspeccion.getPfijos());
        jTextFieldMoviles.setText(inspeccion.getPmoviles());
        jTextPaneResultados.setText(inspeccion.getResultados());
        jTextPaneSustancias.setText(inspeccion.getSustancias());
        jCheckBoxZonaLatente.setSelected(inspeccion.isZlatente());
        jCheckBoxZonaSaturada.setSelected(inspeccion.isZsaturada());
        idInspector=inspeccion.getId_res();
        if (inspeccion.getId_res()!=null)
        {
            Inspector aux= dialogInspectores.getInspector(inspeccion.getId_res());
            if (aux!=null)
            {
                jTextFieldInspector.setText(aux.toString());
            }

        }

        anexosJPanel.setInspeccion(inspeccion);
    }


    public Inspeccion guardarCambios(Inspeccion inspeccion)
    {
         if (inspeccion==null) inspeccion=new Inspeccion();
         inspeccion.setId_res(idInspector);
         inspeccion.setCmax(jTextFieldMax.getText());
         inspeccion.setCmin(jTextFieldMin.getText());

        /** Comprobamos que la fecha de inicio de la inspeccion sea anterior o igual a la fecha fin
         * de la inspeccion. */
        if ((jTextFieldFechaFin.getText().length()>0) && (jTextFieldFechaInicio.getText().length()>0)){
            if (jButtonFechaInicio.getCalendar().getTime().compareTo(jButtonFechaFin.getCalendar().getTime()) > 0){
                JOptionPane.showMessageDialog(parent, messages.getString("jPanelInspeccion.mensaje2"));
                return null;
            }
        }
         if (jTextFieldFechaFin.getText().length()<=0)
             inspeccion.setFfin(null);
         else
             inspeccion.setFfin(jButtonFechaFin.getCalendar().getTime());

         if (jTextFieldFechaInicio.getText().length()<=0)
             inspeccion.setFinicio(null);
         else
             inspeccion.setFinicio(jButtonFechaInicio.getCalendar().getTime());

        /** Comprobamos que la fecha  de recogida de inicio sea anterior o igual a la fecha fin
         * de recogida de datos. */
         if ((jTextFieldFechaRecFin.getText().length()>0) && (jTextFieldFechaRecInicio.getText().length()>0)){
             if (jButtonFechaRecInicio.getCalendar().getTime().compareTo(jButtonFechaRecFin.getCalendar().getTime()) > 0){
                 JOptionPane.showMessageDialog(parent, messages.getString("jPanelInspeccion.mensaje1"));
                 return null;
             }
         }
         if (jTextFieldFechaRecFin.getText().length()<=0)
             inspeccion.setFfindatos(null);
         else
             inspeccion.setFfindatos(jButtonFechaRecFin.getCalendar().getTime());

         if (jTextFieldFechaRecInicio.getText().length()<=0)
             inspeccion.setFinidatos(null);
         else
             inspeccion.setFinidatos(jButtonFechaRecInicio.getCalendar().getTime());

        
         inspeccion.setFriesgo(jTextPaneRiesgos.getText());
         inspeccion.setMedidas(jTextPaneMedidas.getText());
         inspeccion.setMotlantente(jTextPaneZonaLatente.getText());
         inspeccion.setMotsaturada(jTextPaneZonaSaturada.getText());
         try{
         if(jTextFieldNumFolios.getText().length()<=0)
              inspeccion.setNfolios(null);
         else
              inspeccion.setNfolios(jTextFieldNumFolios.getNumber()==null?null:new Integer(jTextFieldNumFolios.getNumber().intValue()));
         }catch (Exception e){};

         try{
         if (jTextFieldRecDias.getText().length()<=0)
              inspeccion.setNrec(null);
         else
              inspeccion.setNrec(jTextFieldRecDias.getNumber()==null?null:new Integer(jTextFieldRecDias.getNumber().intValue()));
         }catch (Exception e){};

         inspeccion.setObs(jTextPaneObs.getText());
         inspeccion.setPfijos(jTextFieldFijos.getText());
         inspeccion.setPmoviles(jTextFieldMoviles.getText());
         inspeccion.setResultados(jTextPaneResultados.getText());
         inspeccion.setSustancias(jTextPaneSustancias.getText());
         inspeccion.setZlatente(jCheckBoxZonaLatente.isSelected());
         inspeccion.setZsaturada(jCheckBoxZonaSaturada.isSelected());
         inspeccion.setAnexos(anexosJPanel.getListaAnexos());

        return inspeccion;
    }
