     private void buscarInspector() {
           dialogInspectores.setLocation(50,50);
           dialogInspectores.setResizable(false);
           if (idInspector!=null) dialogInspectores.load(idInspector);
           dialogInspectores.show();
           if (dialogInspectores.isSeleccionado()&&dialogInspectores.getInspectorSelected()!=null)
           {
               Inspector inspector=dialogInspectores.getInspectorSelected();
               if (inspector!=null)
               {
                  idInspector=inspector.getId();
                  jTextFieldInspector.setText(inspector.toString());
               }
           }
           //dialogInspectores=null;
    }

