	private void seleccionarPersona() {//GEN-FIRST:event_expedientesJTableMouseClicked

		try{
		int seleccion = expedientesJTable.getSelectedRow();
            if (seleccion != -1){
                personaSelected= (CPersonaJuridicoFisica) _personas.get(((Integer)expedientesJTModelSorted.getValueAt(seleccion, hiddenColumn)).intValue());

                dniJTextField.setText((String) expedientesJTModelSorted.getValueAt(seleccion, 0));
                nombreJTextField.setText((String) expedientesJTModelSorted.getValueAt(seleccion, 1));
                apellido1JTextField.setText((String) expedientesJTModelSorted.getValueAt(seleccion, 2));
                apellido2JTextField.setText((String) expedientesJTModelSorted.getValueAt(seleccion, 3));
            }
		}catch(Exception ex){
            logger.error("Exception: "+ex.toString());

        }
	}//GEN-LAST:event_expedientesJTableMouseClicked

	private void buscarJButtonActionPerformed() {//GEN-FIRST:event_buscarJButtonActionPerformed

		Hashtable hash=new Hashtable();
		hash.put("DNI_CIF",dniJTextField.getText());
		hash.put("NOMBRE",nombreJTextField.getText());
		hash.put("APELLIDO1",apellido1JTextField.getText());
		hash.put("APELLIDO2",apellido2JTextField.getText());


		this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));

		_personas = COperacionesLicencias.getSearchedPersonas(hash);

		this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));


		CUtilidadesComponentes.clearTable(expedientesJTableModel);
		if ((_personas == null) || (_personas.size() == 0)) {
			logger.info("personas: " + _personas);
            CConstantesLicencias_LCGIII.persona= null;
			JOptionPane.showMessageDialog(this.getParent(), literales.getString("CPersonaJDialog.mensaje1"));
			return;
		}


		logger.info("personas.size(): " + _personas.size());

		for (int i = 0; i < _personas.size(); i++) {
			CPersonaJuridicoFisica persona = (CPersonaJuridicoFisica) _personas.elementAt(i);
			logger.debug("persona.getDniCif(): " + persona.getDniCif());

			expedientesJTableModel.addRow(new Object[]{persona.getDniCif(), persona.getNombre(), persona.getApellido1(),persona.getApellido2(), new Integer(i)});
		}
	}//GEN-LAST:event_buscarJButtonActionPerformed

