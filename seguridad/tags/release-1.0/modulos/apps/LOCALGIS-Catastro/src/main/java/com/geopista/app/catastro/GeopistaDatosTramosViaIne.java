package com.geopista.app.catastro;

public class GeopistaDatosTramosViaIne 
{
  private int id;
  private int idSeccion;
  private int idSubSeccion;

  public GeopistaDatosTramosViaIne()
  {
  }

  public int getId()
  {
    return id;
  }

  public void setId(int newId)
  {
    id = newId;
  }

  public int getIdSeccion()
  {
    return idSeccion;
  }

  public void setIdSeccion(int newIdSeccion)
  {
    idSeccion = newIdSeccion;
  }

  public int getIdSubSeccion()
  {
    return idSubSeccion;
  }

  public void setIdSubSeccion(int newIdSubSeccion)
  {
    idSubSeccion = newIdSubSeccion;
  }
}