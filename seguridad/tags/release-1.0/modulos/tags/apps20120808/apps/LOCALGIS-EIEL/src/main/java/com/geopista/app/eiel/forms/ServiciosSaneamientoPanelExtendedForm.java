public class ServiciosSaneamientoPanelExtendedForm implements FeatureExtendedForm
{
    
    public ServiciosSaneamientoPanelExtendedForm()
    {  
        
    }
    
    public void setApplicationContext(ApplicationContext context)
    {
        
    }
    
    public void flush()
    {
    }
    
    public boolean checkPanels()
    {
        return true;
    }
    
    
    public AbstractValidator getValidator()
    {
        return null;
    }

    public void disableAll() {
        //To change body of implemented methods use File | Settings | File Templates.
    }

    public void initialize(FeatureDialogHome fd)
    {      
        GeopistaSchema esquema = (GeopistaSchema)fd.getFeature().getSchema();        
        
        AppContext.getApplicationContext().getBlackboard().put("featureDialog", fd);
        
        Object obj = fd.getFeature().getAttribute(esquema.getAttributeByColumn("id"));
        if(obj!=null && !obj.equals("") && 
                ((esquema.getGeopistalayer()!=null && !esquema.getGeopistalayer().isExtracted() && !esquema.getGeopistalayer().isLocal()) 
                        || (esquema.getGeopistalayer()==null) && fd.getFeature() instanceof GeopistaFeature && !((GeopistaFeature)fd.getFeature()).getLayer().isExtracted()))
        {              


        	String codprov = null;
        	if (fd.getFeature().getAttribute(esquema.getAttributeByColumn("codprov"))!=null){
        		codprov=(fd.getFeature().getAttribute(esquema.getAttributeByColumn("codprov"))).toString();
        	}
        	
        	String codmunic = null;
        	if (fd.getFeature().getAttribute(esquema.getAttributeByColumn("codmunic"))!=null){
        		codmunic=(fd.getFeature().getAttribute(esquema.getAttributeByColumn("codmunic"))).toString();
        	}
        	
        	String entidad = null;
        	if (fd.getFeature().getAttribute(esquema.getAttributeByColumn("codentidad"))!=null){
        		entidad=(fd.getFeature().getAttribute(esquema.getAttributeByColumn("codentidad"))).toString();
        	}
        	
        	String nucleo = null;
        	if (fd.getFeature().getAttribute(esquema.getAttributeByColumn("codpoblamiento"))!=null){
        		nucleo=(fd.getFeature().getAttribute(esquema.getAttributeByColumn("codpoblamiento"))).toString();
        	}

            AppContext app =(AppContext) AppContext.getApplicationContext();
            Blackboard Identificadores = app.getBlackboard();
            EdicionOperations operations = new EdicionOperations();
            Identificadores.put("serviciossaneamiento_panel", operations.getPanelServiciosSaneamientoEIEL(codprov,codmunic,entidad,nucleo));
            ServiciosSaneamientoPanel serviciosSaneamientoPanel = new ServiciosSaneamientoPanel();  
            serviciosSaneamientoPanel.loadDataIdentificacion(codprov,codmunic,entidad,nucleo);
            serviciosSaneamientoPanel.loadData();
            this.serviciosSaneamientoPanel = serviciosSaneamientoPanel;           
            fd.addPanel(serviciosSaneamientoPanel);     
        }  
        
}
    
    private ServiciosSaneamientoPanel serviciosSaneamientoPanel = null;

    public void execute() throws Exception 
    {
    	if (serviciosSaneamientoPanel != null) {
    		if(serviciosSaneamientoPanel.datosMinimosYCorrectos()){
    			GeopistaLayer geopistaLayer= (GeopistaLayer) GeopistaEditorPanel.getEditor().getLayerManager().getLayer(ConstantesLocalGISEIEL.NUCLEO_LAYER);
    			String idLayer = geopistaLayer.getSystemId();
    			serviciosSaneamientoPanel.okPressed();
    			ServiciosSaneamientoEIEL servicioSaneamiento = (ServiciosSaneamientoEIEL) AppContext.getApplicationContext().getBlackboard().get("serviciosSaneamiento_panel");
    			ConstantesLocalGISEIEL.clienteLocalGISEIEL.insertarElemento(serviciosSaneamientoPanel.getSaneamientoAutonomo(servicioSaneamiento), idLayer, ConstantesLocalGISEIEL.DATOS_SERVICIOS_SANEAMIENTO);
    			
    			if (EditingInfoPanel.getInstance() != null && EditingInfoPanel.getInstance().getJPanelTree()!=null && EditingInfoPanel.getInstance().getJPanelTree().getPatronSelected()!= null){
    				if (EditingInfoPanel.getInstance().getJPanelTree().getPatronSelected().equals(ConstantesLocalGISEIEL.DATOS_SERVICIOS_SANEAMIENTO)){
    					EditingInfoPanel.getInstance().listarDatosTabla();
    				}
    			}
    		}
    	}
    }

}
