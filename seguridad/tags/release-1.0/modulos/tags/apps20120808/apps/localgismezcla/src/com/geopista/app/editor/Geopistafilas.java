package com.geopista.app.editor;

public class Geopistafilas 
{
  private String enlace;
  private String negado;
  private String campo;
  private String operador;
  private String valor;
  private String orden;

  public String getEnlace()
  {
    return enlace;
  }

  public void setEnlace(String newEnlace)
  {
    enlace = newEnlace;
  }

  public String getNegado()
  {
    return negado;
  }

  public void setNegado(String newNegado)
  {
    negado = newNegado;
  }

  public String getCampo()
  {
    return campo;
  }

  public void setCampo(String newCampo)
  {
    campo = newCampo;
  }

  public String getOperador()
  {
    return operador;
  }

  public void setOperador(String newOperador)
  {
    operador = newOperador;
  }

  public String getValor()
  {
    return valor;
  }

  public void setValor(String newValor)
  {
    valor = newValor;
  }

  public String getOrden()
  {
    return orden;
  }

  public void setOrden(String newOrden)
  {
    orden = newOrden;
  }
}