package com.geopista.app.reports;

public class GeopistaObjetoInformeWhere 
{
  private String orden;
  private String consulta;
  private String etiqueta;
  private String campo;

  public String getOrden()
  {
    return orden;
  }

  public void setOrden(String newOrden)
  {
    orden = newOrden;
  }

  public String getConsulta()
  {
    return consulta;
  }

  public void setConsulta(String newConsulta)
  {
    consulta = newConsulta;
  }

  public String getEtiqueta()
  {
    return etiqueta;
  }

  public void setEtiqueta(String newEtiqueta)
  {
    etiqueta = newEtiqueta;
  }

  public String getCampo()
  {
    return campo;
  }

  public void setCampo(String newCampo)
  {
    campo = newCampo;
  }
}