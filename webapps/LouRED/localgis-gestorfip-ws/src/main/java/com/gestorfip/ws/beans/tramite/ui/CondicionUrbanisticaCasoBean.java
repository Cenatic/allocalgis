package com.gestorfip.ws.beans.tramite.ui;

public class CondicionUrbanisticaCasoBean {
	
	private int id;
	private String nombre;
	private String codigo;
	private int condicionurbanistica;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getNombre() {
		return nombre;
	}
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	public String getCodigo() {
		return codigo;
	}
	
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	
	public int getCondicionurbanistica() {
		return condicionurbanistica;
	}
	
	public void setCondicionurbanistica(int condicionurbanistica) {
		this.condicionurbanistica = condicionurbanistica;
	}

}
